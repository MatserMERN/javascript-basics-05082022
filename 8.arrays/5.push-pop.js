var numbers = [1,2,3]

// push - adds the value at the end of an array 

numbers.push(4)
numbers.push(5)
numbers.push(6)

// unshift - adds the value at the bengining of an array

numbers.unshift(0)
numbers.unshift(-1)
numbers.unshift(-2)

// pop - removes the value at the end of an array 
numbers.pop()
numbers.pop()
numbers.pop()

// shift - removes the value at the bengining of an array
numbers.shift()
numbers.shift()
numbers.shift()

